function loadCSV() {
  fetch("clgdata.csv")
    .then((response) => response.text())
    .then((data) => {
      collegesData = data.split("\n").map((row) =>
        row.split(",").reduce((obj, val, index) => {
          const header = [
            "collegename",
            "department",
            "location",
            "percentage",
          ];
          obj[header[index]] = isNaN(val) ? val.trim() : parseFloat(val);
          return obj;
        }, {})
      );
    });
}

